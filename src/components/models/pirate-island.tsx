import React, { useEffect } from "react";
import { useGraph } from "@react-three/fiber";
import { useGLTF, useAnimations } from "@react-three/drei";
import { SkeletonUtils } from "three-stdlib";
import PirateIsland from "@/assets/captain_ship_island-transformed.glb";
import Ship from "@/assets/ship_custom.glb";
import {
  Mesh,
  SkinnedMesh,
  LoopOnce,
  LoopRepeat,
  AnimationAction,
} from "three";
import { STOPS } from "@/data/portfolio";
import { HOME_DOCK } from "@/data/anchors";
import Marker from "@/components/tour/Marker";
import FloatingVessel from "@/components/env/FloatingVessel";
